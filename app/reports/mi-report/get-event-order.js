const {
  PAYMENT_EXTRACTED,
  PAYMENT_ENRICHED,
  PAYMENT_PAUSED_DEBT,
  PAYMENT_DEBT_ATTACHED,
  PAYMENT_PAUSED_LEDGER,
  PAYMENT_LEDGER_ASSIGNED,
  PAYMENT_PAUSED_QUALITY_CHECK,
  PAYMENT_FAILED_QUALITY_CHECK,
  PAYMENT_PASSED_QUALITY_CHECK,
  PAYMENT_PROCESSED,
  PAYMENT_SUBMITTED,
  PAYMENT_ACKNOWLEDGED,
  PAYMENT_SETTLED
} = require('../../constants/events')

const getEventOrder = (event) => {
  const events = [
    PAYMENT_EXTRACTED,
    PAYMENT_ENRICHED,
    PAYMENT_PAUSED_DEBT,
    PAYMENT_DEBT_ATTACHED,
    PAYMENT_PAUSED_LEDGER,
    PAYMENT_LEDGER_ASSIGNED,
    PAYMENT_PAUSED_QUALITY_CHECK,
    PAYMENT_FAILED_QUALITY_CHECK,
    PAYMENT_PASSED_QUALITY_CHECK,
    PAYMENT_PROCESSED,
    PAYMENT_SUBMITTED,
    PAYMENT_ACKNOWLEDGED,
    PAYMENT_SETTLED
  ]
  return events.indexOf(event) + 1
}

module.exports = {
  getEventOrder
}
