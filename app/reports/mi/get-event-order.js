const {
  PAYMENT_EXTRACTED,
  PAYMENT_ENRICHED,
  PAYMENT_PAUSED_CROSS_BORDER,
  PAYMENT_PAUSED_DEBT,
  PAYMENT_DEBT_ATTACHED,
  PAYMENT_PAUSED_LEDGER,
  PAYMENT_LEDGER_ASSIGNED,
  PAYMENT_PAUSED_QUALITY_CHECK,
  PAYMENT_QUALITY_CHECK_FAILED,
  PAYMENT_QUALITY_CHECK_PASSED,
  PAYMENT_RESET,
  PAYMENT_SUPPRESSED,
  PAYMENT_PROCESSED,
  PAYMENT_SUBMITTED,
  PAYMENT_ACKNOWLEDGED,
  PAYMENT_SETTLED
} = require('../../constants/events')

const getEventOrder = (event) => {
  const eventOrder = [
    PAYMENT_EXTRACTED,
    PAYMENT_ENRICHED,
    PAYMENT_PAUSED_CROSS_BORDER,
    PAYMENT_PAUSED_DEBT,
    PAYMENT_DEBT_ATTACHED,
    PAYMENT_PAUSED_LEDGER,
    PAYMENT_LEDGER_ASSIGNED,
    PAYMENT_PAUSED_QUALITY_CHECK,
    PAYMENT_QUALITY_CHECK_FAILED,
    PAYMENT_QUALITY_CHECK_PASSED,
    PAYMENT_RESET,
    PAYMENT_SUPPRESSED,
    PAYMENT_PROCESSED,
    PAYMENT_SUBMITTED,
    PAYMENT_ACKNOWLEDGED,
    PAYMENT_SETTLED
  ]
  return eventOrder.indexOf(event) + 1
}

module.exports = {
  getEventOrder
}
